<!-- تسجيل الخروج - صفحة تسجيل الخروج من النظام -->
<?php
// بدء جلسة جديدة للتمكن من الوصول لمتغيرات الجلسة الحالية
session_start();

// حذف ملفات تعريف الارتباط (الكوكيز) الخاصة بالمستخدم
// نقوم بتعيين قيمة فارغة وتاريخ انتهاء في الماضي لضمان حذفها
setcookie('user_id', '', time() - 3600, '/');      // حذف كوكي معرف المستخدم
setcookie('user_email', '', time() - 3600, '/');   // حذف كوكي البريد الإلكتروني
setcookie('user_name', '', time() - 3600, '/');    // حذف كوكي اسم المستخدم 
setcookie('user_type', '', time() - 3600, '/');    // حذف كوكي نوع المستخدم

// إنهاء جلسة المستخدم الحالية
session_unset();    // حذف جميع متغيرات الجلسة من الذاكرة
session_destroy();  // إنهاء وتدمير الجلسة الحالية بشكل كامل

// إعادة توجيه المستخدم إلى الصفحة الرئيسية بعد تسجيل الخروج
header("Location: ../index.php");
exit();  // إنهاء تنفيذ الكود للتأكد من عدم تنفيذ أي شيء بعد إعادة التوجيه
?>